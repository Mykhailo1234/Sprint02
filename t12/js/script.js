'use strict'
let res = ""; //змінна без імені

for (let i = 0; i < 10; i++) {   //створюємо цикл поки "i" менше 10,  ‎Інкремент +1
    for (let j = 0; j < i; j++)  //створюємо цикл поки "j" менше "i", Інкремент +1
        res += "*";  //присвоюємо ім'я змінної "*"
    res += "\n";  //висновок з нового рядка, більше або рівно
}
alert(res);  //висновок на екран